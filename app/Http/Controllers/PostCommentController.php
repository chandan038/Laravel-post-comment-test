     */
    public function store(Request $request)
    {
        $post_cmnt = new User_post_comment();
		$post_cmnt->user_id = \Auth::user()->id;
		$post_cmnt->post_id = Input::get('post_id');
		$post_cmnt->comment = Input::get('comment');
        $post_cmnt->save();
		return redirect()->route('show-post', ['id' =>$post_cmnt->post_id])->with('success','Post comment added successfully');
    }

